package src.basics_003_16thDec2023;

public class Lab016 {

    public static void main(String[] args) {

        String s1 = "Akshay";
        //this will store in String constant poll(SCP) or String Pool
        System.out.println(s1);


        //New Operator  -Object
        String s2 = new String("Akshay");
        // this will store in Object area(Heap area)
        //New Operator are used to create the New Object for class
        //it returns of the address of newly created object
        System.out.println(s2);




    }

}
