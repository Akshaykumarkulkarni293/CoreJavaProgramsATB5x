package src.basics_005_23rdDec2023;

public class Lab044 {
    public static void main(String[] args) {

        //For loop flow process

        //   step1: (Initialization) i=1
        //   step2: Condition checking (i<=10)
        //   step3: If condition true, it will execute the code
        //   step4: If condition false, it will stop, program ends
        //   step5: Updation part will happen ( ++i/i++)


        for (int i = 1; i <= 10 ; ++i) {
            System.out.println("value of " +i);

        }
    }
}
