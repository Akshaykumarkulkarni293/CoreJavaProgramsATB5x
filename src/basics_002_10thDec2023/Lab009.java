package src.basics_002_10thDec2023;


//Escape Sequence

public class Lab009 {

    public static void main(String[] args) {

        char c =   '\n'; //next line or new line
        char c1 = '\t'; // Add tab
        char c2 = '\b'; //Backslash delete 1 char from 1st word
        char c3 = '\r'; // Backslash delete 1 word
        System.out.println("Akshay" + c + "Kulkarni");
        System.out.println("Akshay" + c1 + "Kulkarni");
        System.out.println("Akshay" + c2 + "Kulkarni");
        System.out.println("Akshay" + c3 + "Kulkarni");
    }
}
